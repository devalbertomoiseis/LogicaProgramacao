package VariaveisConstantes;

public class TiposDeDados02 {
    public static void main(String[] args) {
        /*
        Tipos:
            - Numérico;
            - Texto;
            - Lógico
        Numéricos:
            - Byte;
            - Short;
            - Integer;
            - Long;
        Numéricos (Decimais):
            - Float;
            - Double;
        Texto:
            - Char;
            - String;
        Lógico:
        - Boolean; Usado para tomar decisões;
        */

        Byte numeroByte = 127; // -128 até 127
        Short numeroShort = 1; // -32.768 até 32.767
        Integer numeroInteger = 1; // -2147483648 até 2147483647
        Long numeroLong = 1L; // -9,223372037x10 a 18 até 9,223372037x10 a 18

        Float numeroFloat = 1.0f;
        Double numeroDouble = 1.0;

    }
}
